# Python 3.6 Tutorial for Beginners
# Working with Numeric Data
# Integers and Floats 

# basic arithmetic operation
num1 = 5
num2 = 3
# addition
add = num1 + num2
print('addtion:', add)
# subtraction
sub = num1 - num2
print('subtraction:', sub)
# multiplication
multi = num1 * num2
print('multiplication:', multi)
# division
div = num1 / num2
print('division:', div)
# floor division
floor_div = num1 // num2
print('floor division:', floor_div)
# exponent
expo = num1 ** num2
print('exponent:', expo)
# modulus
modu = num1 % num2
print('modulus', modu)

# now let's see some method
num = 4
